// Package symbols provides named Unicode symbols for Typst.
//
// This package implements symbol definitions translated from Typst's codex,
// including math symbols, arrows, and emoji references.
//
// Symbols are organized into two main modules:
//   - Sym: General and mathematical symbols
//   - Emoji: Emoji characters
//
// Each symbol may have multiple variants accessed via modifiers.
// For example, arrow.r is the right arrow (→), and arrow.r.double is ⇒.
